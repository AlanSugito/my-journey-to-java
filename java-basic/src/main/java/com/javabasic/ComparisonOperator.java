package com.javabasic;

public class ComparisonOperator {
  public static void main(String[] args) {
    // operator perbandingan adalah operator yang akan membandingkan 2 nilai
    // operator ini akan selalu menghasilkan nilai boolean
    // operatornya adalah
    // >, <, >=, <=, ==, !=
    // e.g
    int operand1 = 10;
    int operand2 = 11;

    System.out.println(operand1 > operand2); // lebih dari
    System.out.println(operand1 < operand2); // kurang dari
    System.out.println(operand1 >= operand2); // lebih dari atau sama dengan
    System.out.println(operand1 <= operand2); // kurang dari atau sama dengan
    System.out.println(operand1 == operand2); // sama dengan
    System.out.println(operand1 != operand2); // tidak sama dengan

  }
}
