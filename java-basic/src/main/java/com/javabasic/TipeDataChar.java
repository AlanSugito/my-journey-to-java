package com.javabasic;

public class TipeDataChar {
  public static void main(String[] args) {
    // char memiliki ukuran 1 byte dan ditulis dengan format '{nilai}', char
    // biasanya cuma satu huruf
    char a = 'A';
    char s = 's';
    char u = 'u';

    System.out.print(a); // print berbeda dengan println, print tidak akan menambah baris baru
    System.out.print(s);
    System.out.print(u);
  }
}
